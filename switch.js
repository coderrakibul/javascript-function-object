//short version of if else

var color = "white";

switch (color){
    case "red" :
        console.log("color is red");
        break;
    case "green" :
        console.log("color is green");
        break;
    case "blue" :
        console.log("color is blue");
        break;
    case "yellow" :
        console.log("color is yellow");
        break;
    default :
    console.log("no color it is black");
}